int	main(int argc, char **argv, char **envp)
{
	t_minishell	shell;

	setlocale(LC_ALL, "");
	print_startup_emote();
	(void)argc;
	(void)argv;
	init_signals();
	if (!start_minishell(&shell, envp))
	{
		ft_putendl_fd("Error initializing minishell\n", STDERR_FILENO);
		exit(EXIT_FAILURE);
	}
	//free_minishell(&shell);
	return (shell.exit_status);
}
