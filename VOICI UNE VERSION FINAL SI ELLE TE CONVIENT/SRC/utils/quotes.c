#include "minishell.h"

void	toggle_quotes(char c, bool *in_sq, bool *in_dq)
{
	if (c == '\'' && !(*in_dq))
		*in_sq = !(*in_sq);
	else if (c == '"' && !(*in_sq))
		*in_dq = !(*in_dq);
}
